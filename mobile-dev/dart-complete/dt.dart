void main(){
  // ============= Data Types =============

  // Strings
  String firstName = "Abanoub";
  print("String: $firstName");

  // Integers
  int myNum = 41;
  print("Integer: $myNum");

  // Doubels
  double otherNum = 44.25;
  print("Double: $otherNum");

  // Booleans
  bool myBool = false;
  print("Boolean: $myBool");

  // Dynamic
  dynamic fullName = "Abanoub Refaat";
  print("Dynamic: $fullName");
}